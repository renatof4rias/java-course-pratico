package Seção_3;

public class Switch {
	public static void main(String[] args) {

		int dia = 0;

		switch (dia) {
		case 1:
			System.out.println("segunda");
			break;
		case 2:
			System.out.println("terca");
			break;
		case 3:
			System.out.println("quarta");
			break;
		case 4:
			System.out.println("quinta");
			break;
		case 5:
			System.out.println("sexta");
			break;

		}
	}
}